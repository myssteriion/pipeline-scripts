package com.es2i.pipeline.job.entities;

public class Tool {

	private String name;
	
	private String value;
	
	
	public Tool(String name, String value) {
		this.name = name;
		this.value = value;
	}



	public String getName() {
		return name;
	}

	public String getValue() {
		return value;
	}
	
}
